# 카테고리 선택
category = st.radio("음식 카테고리 선택", list(food_data.keys()), horizontal=True)

# 추천 버튼
if st.button("🍴 메뉴 추천받기"):
    recommendation = random.choice(food_data[category])
    st.success(f"👉 오늘은 **{recommendation}** 어떠세요?")
else:
    st.info("👆 위에서 카테고리를 고른 뒤 버튼을 눌러주세요!")

